const express = require('express');
const path = require('path');
const favicon = require('serve-favicon');
const logger = require('morgan');
const cookieParser = require('cookie-parser');
const bodyParser = require('body-parser');

const q = require('q');
const mongoose = require('mongoose');
mongoose.connect('mongodb://localhost/find_me');
db = mongoose.connection;
db.once('open', function() {
  console.log('mongodb connected');
});
mongoose.Promise = q.Promise;

const routes = require('./routes/index');
const users = require('./routes/users');
const apiresponse = require('./routes/APIResponses');
const mloutputs = require('./routes/MLOutputs');
const bases = require('./routes/Bases');
const tmp = require('./routes/tmp');

const app = express();

// view engine setup
app.set('views', path.join(__dirname, 'views'));
app.set('view engine', 'ejs');

// uncomment after placing your favicon in /public
//app.use(favicon(path.join(__dirname, 'public', 'favicon.ico')));
app.use(logger('dev'));
app.use(bodyParser.json());
app.use(bodyParser.urlencoded({ extended: false }));
app.use(cookieParser());
app.use(express.static(path.join(__dirname, 'public')));

app.use('/', routes);
app.use('/users', users);
app.use('/apiresponses', apiresponse);
app.use('/mloutputs', mloutputs);
app.use('/bases', bases);
app.use('/tmp', tmp);

// catch 404 and forward to error handler
app.use(function(req, res, next) {
  let err = new Error('Not Found');
  err.status = 404;
  next(err);
});

// error handlers

// development error handler
// will print stacktrace
if (app.get('env') === 'development') {
  app.use(function(err, req, res, next) {
    res.status(err.status || 500);
    res.render('error', {
      message: err.message,
      error: err
    });
  });
}

// production error handler
// no stacktraces leaked to user
app.use(function(err, req, res, next) {
  res.status(err.status || 500);
  res.render('error', {
    message: err.message,
    error: {}
  });
});


module.exports = app;
